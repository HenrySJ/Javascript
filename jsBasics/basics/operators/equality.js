// Strick Equality (type + Value)
console.log(1 === 1); // True
console.log('1' === 1); // False, different type, string w/ number

// Lose Equality (Value)
console.log(1 == 1); // True
console.log('1' == 1); // True

// Lose equality will change type of right comparison to that of left comparison and only check the value
